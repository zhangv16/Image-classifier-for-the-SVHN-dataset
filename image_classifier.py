def get_CNN_model(wd):
    model = Sequential([
        Conv2D(filters=10,
               kernel_size=3,
               padding='SAME',
               activation='relu',
               input_shape=(32, 32, 1)),
        MaxPool2D(pool_size=(3, 3)),
        Conv2D(filters=10,
               kernel_size=3,
               padding='SAME',
               activation='relu'),
        MaxPool2D(pool_size=(3, 3)),
        Flatten(),
        BatchNormalization(),
        Dense(128, activation='relu', kernel_regularizer=regularizers.l2(wd)),
        Dense(128, activation='relu', kernel_regularizer=regularizers.l2(wd)),
        Dense(10, activation='softmax')
    ])
    return model

# Get CNN model.

